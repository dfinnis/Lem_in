char	*ft_strrev(char *str)
{
	int i;
	int	j;
	int swap;

	j = 0;
	while (str[j])
		j++;
	i = -1;
	while (++i < --j)
	{
		swap = str[i];
		str[i] = str[j];
		str[j] = swap;
	}
	return (str);
}
